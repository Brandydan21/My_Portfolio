            <h2 id="topic5">What are related technologies?</h2>
            <p>There is a lot that goes on behind the scenes of videos that viewers watch, we simply only see the surface and the final output from all the action behind the transferring of data and actually streaming that data for viewers to see. Listed below is what a typical setup of video streaming looks like (though it can vary depending on the specific tools you use) from the point of the video content being recorded to the content being viewed from the user end:
            </p>
            <ol>
                <li>The RAW video gets captured by the camera</li>
                <li>The video then gets sent to an encoder</li>
                <li>This will cover the file into a digital file</li>
                <li>Gets ingested into an online video platform</li>
                <li>From there, it then gets distributed to the video player</li>
                <li>Finally, the stream will display on the users device</li>
             </ol>
            <p>To help with creating a seamless streaming experience, different streaming technologies are used to help cater to this, below of which are four major ones that are used. These streaming technologies work together to transmit the video content from point A to point B:</p>
            <dl>
                <li>Streaming protocols</li>
                <li>Codecs (coder-decoder)</li>
                <li>Video players</li>
                <li>Content delivery networks (CDN)</li>
            </dl>
            <h3>Streaming protocols:</h3>
            <p>Throughout the streaming process, the streaming protocols are what helps to carry the contents from one destination to the next and is a standardized method of delivering media across the internet. There are a lot of important streaming protocols that are used but the following ones are the protocols that are more commonly used today: HLS, RTMP, RTSP, MPEG-DASH</p>
            <h3>Codecs:</h3>
            <p>There are two types of codecs, audio and video codecs. Due its bulky state, RAW video files are converted into a digital file through codecs so that it can be streamable to the user. To make sure that it&#39;s easy to transfer the files over the internet, the contents are packed up and compacted before it gets transported from one place to another. <p>
            <h3>Video players:</h3>
            <p>This is the user-facing technology where viewers are able to view the content. Founded by Apple, the HTML5 video player is the standard for most live streaming platforms and can be supported on pretty much any internet-enabled device like smartphones, tablets, etc. In addition to this, it is also compatible with most browsers and operating systems and is very secure and easily customizable.</p>
            <h3>Content delivery networks (CDN)</h3>
            <p>They are servers that are strategically placed over a geographical region to deliver high-quality content to locations that are remote from the stream&#39;s source. The content is sent to a network of servers before redirecting the request from the originating site&#39;s server to the server in the CDN that is closest to the user (upon the user&#39;s desired selection). When the originating point and the destinations are placed closer to each other, the content can be delivered faster.</p>
