            <p>The main purpose of streaming media is entertainment. It can be used to consume media for entertainment, as well as being used for communicating with others.</p>
            <p>It allows for people to watch videos or listen to music without downloading the entire video or song, because it can be streamed as it is being played. Live streaming is a popular part of streaming media, with many different people and groups broadcasting content to a live audience on platforms like Twitch.</p>
            <p>A major feature of streaming media is that it can be accessed from any device that supports it (almost all modern devices), and allows for media to be much more accessible to those with an internet connection than physical media forms. It also does not require downloading or owning a physical copy, which means storage isn&#39;t a problem.</p>
            <p>Streaming media can also be shared around easily because it is usually just a link that others can open to view the same content as you. Streaming media buffers the content, loading and playing it in chunks of data rather than downloading the entire thing first, then playing it back. Streaming media can be flexible, as a faster connection usually allows for a higher resolution to be sent over and more data to be buffered before it gets played back.</p>
