            <h2 id="topic2">Who developed it?</h2>
            <p>The first ever streaming devices were radios, which transmitted manipulated electrical frequencies between two places without the use of wires. With the help of many physicists, Guglielmo Marconi was able to develop a way to transmit these radio signals in 1895. These signals were generally used for military purposes, transmitting morse code signals.</p>
            <p>It was only in 1920, when U.S Major General George O. Squier found a way to transmit and distribute electrical signals to recreate sounds, allowing for music to be transmitted through electrical wires. Eventually the radio we have today, which transmitted music without the need of wires, the television, as well as the internet and computers were developed, allowing for the streaming of media such as audio, videos and images.</p>
            <p>Since the mid-20th century, people have attempted to display different types of media on computers, from images, audio and videos. However, due to the hardware limitations, as well of the high cost of hardware, streaming content and media was proven very difficult, instead content such as audio and video were usually delivered as a playback from the user&#39;s local hard disk drive. </p>
            <p>In the late 1990s and early 2000, as more users had access to the internet, increased network bandwidth, improvements in computer hardware, combined with increased investments into the internet and its technologies, streaming media was developed and was accessible to users of the internet. </p>
            <p>The first ever live streamed video on the internet was a performance from the band Severe Tire Damage on June 24, 1993. Nowadays, streaming media is used in everyday life, from video conferences such as Zoom, to live streaming content on Twitch or streaming video media from Youtube or TV shows from Netflix.</p>
